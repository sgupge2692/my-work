#include <iostream>
#include <cmath>
using namespace std;

int main() {
    int number;
    cin >> number;

    // 各桁をバラバラにして配列に格納
    int a[4];
    a = number / 100;          // 百の位 (例: 123 -> 1)
    a[1] = (number / 10) % 10;    // 十の位 (例: 123 -> 2)
    a[2] = number % 10;           // 一の位 (例: 123 -> 3)

    // バブルソート：昇順（小さい順）に並べ替える
    int tmp = 0;
    for (int i = 0; i < 3; i++) {
        for (int j = i + 1; j < 3; j++) {
            if (a[i] > a[j]) {
                tmp = a[i];
                a[i] = a[j];
                a[j] = tmp;
            }
        }
    }

    // ソート後：aが最小、a[2]が最大
    // 大きい順に並べた数
    int max_num = a[2] * 100 + a[1] * 10 + a;
    // 小さい順に並べた数
    int min_num = a * 100 + a[1] * 10 + a[2];

    int result = max_num - min_num;

    cout << result << endl;

    return 0;
}
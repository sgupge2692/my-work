#include <iostream>
#include <cmath>
using namespace std;

int main() {
    // 署名がない場合は減点されるため注意 [5]
    int usage;
    if (!(cin >> usage)) return 0; // 入力ミスは想定しなくてよい [5]

    int fee = 1200; // 基本料金

    // 累進制（段階的）な料金計算 [1][2]
    if (usage > 30) {
        // 30m3を超えた分の単価は250円。
        // それに加えて、10m3から30m3までの20m3分（3000円）を加算する
        fee += (20 * 150) + (usage - 30) * 250;
    } else if (usage > 10) {
        // 10m3を超え30m3以下の分は単価150円
        fee += (usage - 10) * 150;
    }
    // 10m3以下の場合は基本料金のみ（超過料金は0円）

    // 消費税10%を加算し、小数点以下を切り捨てる [4]
    int total = (int)(fee * 1.1);

    cout << total << endl;

    return 0;
}
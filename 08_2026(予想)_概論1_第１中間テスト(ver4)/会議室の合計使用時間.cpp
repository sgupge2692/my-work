#include <iostream>
using namespace std;

int main() {
    // 0〜99の各1時間枠を管理する配列（0で初期化） [2]
    int flag[100] = {0}; 
    int start, end;

    // 3人分の入力 [2]
    for(int i = 0; i < 3; i++) {
        cin >> start >> end;
        // 出勤時刻から退勤時刻までの各1時間スロットにフラグを立てる [1]
        // 例：1h〜5hなら、1, 2, 3, 4のスロットを「使用中」とする
        for(int j = start; j < end; j++) {
            flag[j] = 1;
        }
    }

    int total = 0;
    // 0時から99時までの各スロットをチェック
    for(int i = 0; i < 100; i++) {
        // フラグが0（誰もいない）時間をカウント
        if(flag[i] == 0) {
            total++;
        }
    }

    if(total > 0) {
        cout << total << "時間分だけ誰も働いていない時間があります。" << endl;
    } else {
        cout << "誰も働いていない時間はありません。" << endl;
    }

    return 0;
}
#include <iostream>
using namespace std;

int main() {
    // 4x4の2次元配列として正しく宣言する [3]
    int mat[4][4]; 
    for(int i = 0; i < 4; i++) {
        for(int j = 0; j < 4; j++) cin >> mat[i][j];
    }

    for(int i = 0; i < 4; i++) {
        for(int j = 0; j < 4; j++) {
            // 三項演算子を使い、外枠（i,jが0または3）なら値を、それ以外は0を表示 [4],[3]
            cout << ((i == 0 || i == 3 || j == 0 || j == 3) ? mat[i][j] : 0) << (j == 3 ? "" : " ");
        }
        cout << endl;
    }
    return 0;
}
/*********************************************************
//もう一回　5つの整数を読み込み、バブルソート（昇順）を用いて並べ替えなさい。
 ただし、ソートの「外側のループが1回終わるごと」に、その時点での配列の状態を1行に表示すること。
**********************************************************/
#include <iostream>
using namespace std;

int main() {
    int a[5];
    for (int i = 0; i < 5; i++) {
        cin >> a[i];
    }

    // バブルソート：隣り合う要素を比較する
    for (int i = 0; i < 4; i++) { // 5つの要素なら4回で確定
        for (int j = 0; j < 4 - i; j++) { // 右端から確定していくため 4-i
            if (a[j] > a[j + 1]) {
                int tmp = a[j];
                a[j] = a[j + 1];
                a[j + 1] = tmp;
            }
        }
        
        // 1ループ終了ごとの表示
        for (int k = 0; k < 5; k++) {
            cout << a[k] << (k == 4 ? "" : " ");
        }
        cout << endl;
    }

    return 0;
}